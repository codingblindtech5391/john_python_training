# Variable Example with f-strings

# Integer variable
age = 25
print(f"My age is: {age}")

# String variable
name = "John"
print(f"My name is: {name}")

# Float variable
salary = 50000.50
print(f"My salary is: {salary}")

# Boolean variable
is_student = True
print(f"I am a student: {is_student}")

# Updating variables
age = age + 5
print(f"After 5 years, my age will be: {age}")

# Concatenating strings with f-strings
greeting = "Hello, "
full_greeting = f"{greeting}{name}"
print(full_greeting)
